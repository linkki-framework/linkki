package org.linkki.samples.playground.ts.table;

public class Person {

    private final String name;
    private int age;
    private int likes = 0;

    public Person(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public int getLikes() {
        return likes;
    }

    public void like() {
        this.likes++;
    }
}
